function max(num1, num2)

   if (num1 > num2) then
      result = num1;
   else
      result = num2;
   end

   return result;
end

function min(num1, num2)

   if (num1 < num2) then
      result = num1;
   else
      result = num2;
   end

   return result;
end

function sum(num1, num2)

   return num1 + num2;
end

function sub(num1, num2)

   return num1 - num2;
end
